Reflexión:

- Como escribo?

-- Yo suelo escribir de una manera muy ordenada y clara, suelo tener mucho cuidado con la ortografía, con la redacción clara y consisa, también con el uso de signos como las comas, puntos, etc. Suelo redactarla muy clara.

- Como cambia mi voz (tanto escriba como hablada), dependiendo del entorno y el contexto en el que me encuentre?

-- Suele cambiar en la manera de tono, explicación y la manera de pensar, cuando escribo suele ser de la misma estructura.